/**
 * Normalize city/province names for lookup
 */
function normalizeName(name: string): string {
  return name
    .toLowerCase()
    .trim()
    .replace(/\s+/g, " ")
    .replace(/city\s+of\s+/i, "")
    .replace(/\s+city$/i, "")
    .replace(/\s*\([^)]*\)\s*/g, "") // Remove parentheses and their contents
    .replace(/\s+/g, " ") // Normalize multiple spaces
    .trim();
}

/**
 * Lookup zip code for a city/municipality in a province
 */
export function lookupZipCode(provinceName: string, cityName: string): string | null {
  if (!provinceName || !cityName) {
    return null;
  }

  const normalizedProvince = normalizeName(provinceName);
  const normalizedCity = normalizeName(cityName);

  // Try exact match first (case-sensitive)
  if (zipCodeMap[provinceName] && zipCodeMap[provinceName][cityName]) {
    return zipCodeMap[provinceName][cityName];
  }

  // Try normalized lookup - exact normalized match
  for (const [prov, cities] of Object.entries(zipCodeMap)) {
    const normalizedProv = normalizeName(prov);
    if (normalizedProv === normalizedProvince) {
      for (const [city, zipCode] of Object.entries(cities)) {
        const normalizedCityName = normalizeName(city);
        if (normalizedCityName === normalizedCity) {
          return zipCode;
        }
      }
    }
  }

  // Try case-insensitive exact match
  for (const [prov, cities] of Object.entries(zipCodeMap)) {
    if (prov.toLowerCase() === provinceName.toLowerCase()) {
      for (const [city, zipCode] of Object.entries(cities)) {
        if (city.toLowerCase() === cityName.toLowerCase()) {
          return zipCode;
        }
      }
    }
  }

  // Try partial match (city name contains or is contained) - more lenient
  for (const [prov, cities] of Object.entries(zipCodeMap)) {
    const normalizedProv = normalizeName(prov);
    const provLower = prov.toLowerCase();
    const provinceLower = provinceName.toLowerCase();
    
    if (normalizedProv === normalizedProvince || 
        normalizedProv.includes(normalizedProvince) || 
        normalizedProvince.includes(normalizedProv) ||
        provLower === provinceLower ||
        provLower.includes(provinceLower) ||
        provinceLower.includes(provLower)) {
      for (const [city, zipCode] of Object.entries(cities)) {
        const normalizedCityName = normalizeName(city);
        const cityLower = city.toLowerCase();
        const cityNameLower = cityName.toLowerCase();
        
        if (normalizedCityName === normalizedCity ||
            normalizedCityName.includes(normalizedCity) ||
            normalizedCity.includes(normalizedCityName) ||
            cityLower === cityNameLower ||
            cityLower.includes(cityNameLower) ||
            cityNameLower.includes(cityLower)) {
          return zipCode;
        }
      }
    }
  }

  return null;
}
